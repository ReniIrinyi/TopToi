<template>
  <header class="header">
    <div class="logo">
      <h1>TopToi</h1>
    </div>
    <div class="auth-section">
      <div class="profile-dropdown" @click="toggleDropdown">
        <img class="avatar" :src="userAvatar" alt="avatar" />
        <div class="dropdown" v-if="dropdownOpen">
          <template v-if="!isLoggedIn">
            <button @click="showLogin = true">{{t('LABEL_LOGIN')}}</button>
            <button @click="showRegister = true" class="secondary">{{ t('LABEL_REGISTRATION') }}</button>
          </template>
          <template v-else>
            <p><strong>{{ userName }}</strong></p>
            <p>{{ userEmail }}</p>
            <hr />
            <button @click="logout">{{t('LABEL_LOGOUT')}}</button>
          </template>
        </div>
      </div>
    </div>
    <div class="language-selector" @click="toggleLanguageDropdown">
      <span class="flag-icon">{{ getFlagIcon(currentLanguage) }}</span>
      <div class="language-dropdown" v-if="languageDropdownOpen">
        <div
            v-for="lang in getAvailableLanguages()"
            :key="lang"
            class="language-option"
            @click.stop="changeLanguage(lang)"
        >
          {{ getFlagIcon(lang) }}
        </div>
      </div>
    </div>


    <LoginDialog v-if="showLogin" @close="showLogin = false" @logged-in="onLogin" />
    <RegisterDialog @logged-in="onLogin" v-if="showRegister" @close="showRegister = false"  />
  </header>
</template>

<script setup>
import { ref, computed, onMounted } from 'vue';
import LoginDialog from './LoginForm.vue';
import RegisterDialog from './RegisterForm.vue';
import { translate, getLanguage, setLanguage } from '@/service/translationService.js';

const showLogin = ref(false);
const showRegister = ref(false);
const dropdownOpen = ref(false);
const languageDropdownOpen = ref(false);
const currentLanguage = ref(getLanguage());

const userEmail = ref(localStorage.getItem('userEmail') || null);
const userName = ref(localStorage.getItem('userName') || 'Felhasználó');
const userAvatar = ref(localStorage.getItem('userAvatar') || 'https://www.gravatar.com/avatar?d=mp');

const t = translate;

const emit = defineEmits(['onAuthChange'])

const isLoggedIn = computed(() => {
  return !!localStorage.getItem('token');
});

function onLogin(data) {
  console.log(data)
  userEmail.value = data.email;
  userName.value = data.name
  userAvatar.value = data.imgUrl || data.img || 'https://www.gravatar.com/avatar?d=mp';
  showLogin.value = false;
  emit('onLogin')
}

function toggleDropdown() {
  dropdownOpen.value = !dropdownOpen.value;
}

function logout() {
  localStorage.clear();
  userEmail.value = null;
  userName.value = '';
  userAvatar.value = '';
  dropdownOpen.value = false;
  location.reload();
  emit('onAuthChange')
}

function getFlagIcon(lang) {
  switch (lang) {
    case 'hu': return '🇭🇺';
    case 'en': return '🇬🇧';
    case 'de': return '🇩🇪';
    default: return '🌐';
  }
}

function getAvailableLanguages() {
  return ['hu', 'en', 'de'].filter(lang => lang !== currentLanguage.value);
}

function toggleLanguageDropdown() {
  languageDropdownOpen.value = !languageDropdownOpen.value;
}

function changeLanguage(lang) {
  currentLanguage.value = lang;
  setLanguage(lang);
  languageDropdownOpen.value = false;
}

onMounted(() => {
  console.log('component Header rendered...');
});
</script>

